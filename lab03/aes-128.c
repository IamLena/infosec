unsigned char *getkey(char *keyfile)
{
	unsigned char *K = NULL;
	int keyfd = open(keyfile, O_RDONLY);
	if (keyfd > 0)
	{
		K = malloc(keysize);
		if (K)
		{
			int res = read(keyfd, K, keysize);
			if (res != keysize)
			{
				free(K);
				K = NULL;
			}
		}
		close(keyfd);
	}
	return (K);
}

unsigned char *expandkeys(unsigned char *K)
{
	if (!K)
		return (NULL);
	unsigned char *roundkeys = malloc((nrounds + 1) * 16);
	if (!roundkeys)
	{
		free(K);
		return (NULL);
	}
	int i = 0;
	while (i < keysize) // i < 16
	{
		roundkeys[i] = K[i];
		i++;
	}
	while (i <= (nrounds + 1) * 16 - 4)
	{
		// Wi = Wi-1
		roundkeys[i] = roundkeys[i - 4];
		roundkeys[i + 1] = roundkeys[i - 3];
		roundkeys[i + 2] = roundkeys[i - 2];
		roundkeys[i + 3] = roundkeys[i - 1];

		if (i % 16 == 0)
		{
			//left shift
			unsigned char tmp = roundkeys[i];
			roundkeys[i] = roundkeys[i + 1];
			roundkeys[i + 1] = roundkeys[i + 2];
			roundkeys[i + 2] = roundkeys[i + 3];
			roundkeys[i + 3] = tmp;

			//subbox
			roundkeys[i] = SubBox[roundkeys[i]];
			roundkeys[i + 1] = SubBox[roundkeys[i + 1]];
			roundkeys[i + 2] = SubBox[roundkeys[i + 2]];
			roundkeys[i + 3] = SubBox[roundkeys[i + 3]];

			// xor Rcon
			roundkeys[i] ^= Rcon[i / 16];
			// roundkeys[i + 1] ^= 0;
			// roundkeys[i + 2] ^= 0;
			// roundkeys[i + 3] ^= 0;

		}

		// Wi xor Wi-4
		roundkeys[i] ^= roundkeys[i - 16];
		roundkeys[i + 1] ^= roundkeys[i - 15];
		roundkeys[i + 2] ^= roundkeys[i - 14];
		roundkeys[i + 3] ^= roundkeys[i - 13];

		i += 4;
	}
	return (roundkeys);
}

unsigned char	*getmsgblock(char *filename, int *fd, unsigned char **M)
{
	if (*fd == -1)
	{
		*fd = open(filename, O_RDONLY);
		if (*fd == -1)
		{
			if (*M)
				free(*M);
			return NULL;
		}
	}
	if (*M == NULL)
		*M = malloc(msgsize);
	if (*M)
	{
		int res = read(*fd, *M, msgsize);
		if (res == -1 || res == 0)
		{
			free(*M);
			*M = NULL;
			close(*fd);
			return (*M);
		}
		while(res < msgsize)
		{
			(*M)[res] = 0;
			res++;
		}
	}
	return (*M);
}

unsigned char galua_mul(unsigned char byte1, unsigned char byte2)
{
	if (byte2 == 0 || byte1 == 0)
		return 0;
	int res = 0;
	if (byte2 & 0b00000001)
		res = (int)byte1;
	byte2 = byte2 >> 1;
	while (byte2 != 0)
	{
		if (byte1 & 0b10000000)
			byte1 = (byte1 << 1) ^ 0b00011011;
		else
			byte1 = byte1 << 1;
		if (byte2 & 0b00000001)
			res = res ^ byte1;
		byte2 = byte2 >> 1;
	}
	return res;
}

void MixColumns(unsigned char *M)
{
	for (int j = 0; j < 4; j++) // column j of M (one word)
	{
		unsigned char newcolumn[4] = {0, 0, 0, 0}; // new word
		for (int i = 0; i < 4; i++)
		{
			for (int k = 0; k < 4; k++) // go through elements of C-row / M-column
				newcolumn[i] = newcolumn[i] ^ galua_mul(M[j  * 4 + k], C[i * 4 + k]);
		}
		for (int i = 0; i < 4; i++)
			M[j * 4 + i] = newcolumn[i];
	}
}

void MixColumns_inv(unsigned char *M)
{
	for (int j = 0; j < 4; j++) // column j of M (one word)
	{
		unsigned char newcolumn[4] = {0, 0, 0, 0}; // new word
		for (int i = 0; i < 4; i++)
		{
			for (int k = 0; k < 4; k++) // go through elements of C-row / M-column
				newcolumn[i] = newcolumn[i] ^ galua_mul(M[j  * 4 + k], InvC[i * 4 + k]);
		}
		for (int i = 0; i < 4; i++)
			M[j  * 4 + i] = newcolumn[i];
	}
}

void	crypt(unsigned char *M, unsigned char *roundkeys)
{
	for (int byte = 0; byte < 16; byte++)
			M[byte] ^= roundkeys[byte];

	for (int curround = 1; curround <= nrounds; curround++)
	{
		// subbytes
		for (int byte = 0; byte < 16; byte++)
			M[byte] = SubBox[M[byte]];

		//shiftrows left
		unsigned char tmp1;
		unsigned char tmp2;
		unsigned char tmp3;

		tmp1 = M[1];
		M[1] = M[5];
		M[5] = M[9];
		M[9] = M[13];
		M[13] = tmp1;

		tmp1 = M[2];
		tmp2 = M[6];
		M[2] = M[10];
		M[6] = M[14];
		M[10] = tmp1;
		M[14] = tmp2;

		tmp1 = M[3];
		tmp2 = M[7];
		tmp3 = M[11];
		M[3] = M[15];
		M[7] = tmp1;
		M[11] = tmp2;
		M[15] = tmp3;

		if (curround != nrounds)
		{
			//mixcolumns
			MixColumns(M);
		}

		// xor Kcurround
		for (int byte = 0; byte < 16; byte++)
			M[byte] ^= roundkeys[curround * 16 + byte];
	}
}

void	decrypt(unsigned char *M, unsigned char *roundkeys)
{
	for (int byte = 0; byte < 16; byte++)
		M[byte] ^= roundkeys[nrounds * 16 + byte];

	for (int curround = nrounds - 1; curround >= 0; curround--)
	{
		//InvShiftRows right
		unsigned char tmp1;
		unsigned char tmp2;
		unsigned char tmp3;

		tmp1 = M[1];
		tmp2 = M[5];
		tmp3 = M[9];
		M[1] = M[13];
		M[5] = tmp1;
		M[9] = tmp2;
		M[13] = tmp3;

		tmp1 = M[2];
		tmp2 = M[6];
		M[2] = M[10];
		M[6] = M[14];
		M[10] = tmp1;
		M[14] = tmp2;

		tmp1 = M[3];
		M[3] = M[7];
		M[7] = M[11];
		M[11] = M[15];
		M[15] = tmp1;

		// InvSubBytes
		for (int byte = 0; byte < 16; byte++)
			M[byte] = InvSubBox[M[byte]];

		// xor Kcurround
		for (int byte = 0; byte < 16; byte++)
			M[byte] ^= roundkeys[curround * 16 + byte];

		if (curround != 0)
		{
			// InvMixColumns
			MixColumns_inv(M);
		}
	}
}

int main(int argc, char **argv)
{
	unsigned char *K = NULL;
	unsigned char *M = NULL;
	int msgfd = -1;
	int resfd = -1;
	unsigned char *roundkeys = NULL;

	if (argc != 5)
		return (returnerror("./aes keyfile msgfile resfile c/d", 1));
	if (argv[4][0] != 'c' && argv[4][0] != 'd')
		return (returnerror("./aes keyfile msgfile resfile c/d", 1));
	if (!resfd)
		return (returnerror("res file error\n", 2));
	K = getkey(argv[1]);
	if (!K)
		return (returnerror("key error\n", 3));
	roundkeys = expandkeys(K);

	if (!roundkeys)
		return (returnerror("key expansion error\n", 4));
	resfd = open(argv[3], O_WRONLY | O_CREAT);
	while (getmsgblock(argv[2], &msgfd, &M) != NULL)
	{
		if (argv[4][0] == 'c')
			crypt(M, roundkeys);
		else if (argv[4][0] == 'd')
			decrypt(M, roundkeys);
		write(resfd, M, msgsize);
	}
	free(K);
	free(roundkeys);
	close(resfd);
	return (0);
}
