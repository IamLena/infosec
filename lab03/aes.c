unsigned char *getkey(char *keyfile)
{
	unsigned char *K = NULL;
	int keyfd = open(keyfile, O_RDONLY);
	if (keyfd > 0)
	{
		K = malloc(keysize);
		if (K)
		{
			int res = read(keyfd, K, keysize);
			if (res != keysize)
			{
				free(K);
				K = NULL;
			}
		}
		close(keyfd);
	}
	return (K);
}

unsigned char *getmsgblock(int fd)
{
	unsigned char *M = malloc(16);
	if (M)
	{
		int res = read(fd, M, 16);
		if (res > 0)
		{
			while (res < 16)
			{
				M[res] = 0;
				res++;
			}
		}
		else
		{
			free (M);
			M = NULL;
		}
	}
	return (M);
}

